#area function
def area(width, height):
    result = width * height
    return result

result = area(5, 6)
print(result)

print(area(8, 5))
print(area(10, 3))

#subtraction funcition
def subtract(num1, num2):
    result = num1 - num2
    return result

print(subtract(7, 5))
print(subtract(200, 10))

#add fucntion
def add(num1, num2):
    return num1 + num2

print(add(2, 2))
print(add(4, 9))



